#include "mg51_sdcc.h"

/************************************************************************************************************
*    TIMER 3 interrupt subroutine
************************************************************************************************************/
void Timer3_ISR (void) __interrupt (16)
{
    PUSH_SFRS;
  	
    clr_T3CON_TF3;
    P12 ^= 1;

    POP_SFRS;
}	

 /************************************************************************************************************
*    Main function 
************************************************************************************************************/
void main (void)
{
    
	MODIFY_HIRC(HIRC_24);
	P12_QUASI_MODE;

    Timer3_AutoReload_Interrupt_Initial(24,6400);
    ENABLE_GLOBAL_INTERRUPT;

    while(1);
}

